import Foundation
import UIKit

extension UIFont {
    static func Figtree_Regular(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-Regular", size: size)
    }
    static func Figtree_Medium(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-Medium", size: size)
    }
    static func Figtree_Bold(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-Bold", size: size)
    }
    static func Figtree_Light(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-Light", size: size)
    }
    static func Figtree_ExtraBold(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-ExtraBold", size: size)
    }
    static func Figtree_Black(size: CGFloat) -> UIFont? {
        return UIFont(name :"Figtree-Black", size: size)
    }
    static func Figtree_SemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name :"figtree-semibold", size: size)
    }
}
